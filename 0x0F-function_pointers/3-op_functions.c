#include "3-calc.h"

/**
 * op_add - addition
 * @a: no 1
 * @b: no 2
 *
 * Return: sum
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - subtraction
 * @a: no 1
 * @b: no 2
 *
 * Return: difference
 */

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - multiplication
 * @a: no 1
 * @b: no 2
 *
 * Return: multiple
 */

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - division
 * @a: no 1
 * @b: no 2
 *
 * Return: division result
 */

int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - modulus
 * @a: no 1
 * @b: no 2
 *
 * Return: modulus
 */

int op_mod(int a, int b)
{
	return (a % b);
}
